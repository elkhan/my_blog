const [first, second, third] = ['Laide', 'Gabriel', 'Jets']

console.log(first) // Laide
console.log(second) // Gabriel
console.log(third) // Jets

const thing = ['Table', 'Chair', 'Fan']

const [a, b, c] = thing

console.log(a) // Table
console.log(b) // Chair
console.log(c) // Fan
